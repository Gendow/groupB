package useraccount;

import java.util.Date;
import java.util.List;

public class Journalist extends Citizen {
	
	private String organization;
	
	public Journalist(String userName,
					String password,
					Date creationDate,
					List<String> capabilities,
					String firstName,
					String lastName,
					String gender,
					String dateOfBirth,
					String location,
					String organization) {
		super(userName, password, creationDate, capabilities, firstName, lastName, gender, dateOfBirth, location);
		this.organization = organization;
	}

	public String getOrganization() {
		return organization;
	}

	public void setOrganization(String organization) {
		this.organization = organization;
	}
}
